import requests
from bs4 import BeautifulSoup

# returns hour and min in tuple from row
def get_time_from_row(row):
    row_data = row.find_all(class_="r") # print(row_data)
    date_time = str(row_data[0].get_text()).split()
    city_time = date_time[1].split(":")
    return city_time[0], city_time[1]

def get_url_of_best_noon():
    sun_zenith_url = 'http://www.timeanddate.com/worldclock/sunearth.html'
    response = requests.get(sun_zenith_url)
    html = response.content
    soup = BeautifulSoup(html, "html.parser")

    # get table data
    table = soup.find('div', class_="row tb-scroll")
    rows = table.find_all('tr')[1:]

    # if hour is ending choose the first one
    city_hour, city_min = get_time_from_row(rows[0])
    if city_hour == '11' and int(city_min) >= 50:
        return('http://www.timeanddate.com' + rows[0].find('a').get('href'))

    # search for row with 12
    for row in rows:
        city_hour, city_min = get_time_from_row(row)
        if city_hour == '12':
            city_tag = row.find('a')
            return('http://www.timeanddate.com' + city_tag.get('href'))

    # if no 12 available choose 1st one
    return('http://www.timeanddate.com' + rows[0].find('a').get('href'))

def high_noon():
    city_url = get_url_of_best_noon()
    response = requests.get(city_url)
    html = response.content
    soup = BeautifulSoup(html, "html.parser")

    title = soup.find('title')
    strip_length = len('Current Local Time in ')
    return("It's ***High Noon*** in **" + title.get_text()[strip_length:] + "**")


